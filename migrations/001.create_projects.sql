
CREATE TABLE projects (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    t_volume INTEGER NOT NULL,
    canna_id INTEGER REFERENCES cannabinoids(id) ON DELETE CASCADE NOT NULL,
    canna_concentration INTEGER NOT NULL,
    carrier_id INTEGER REFERENCES carriers(id) ON DELETE CASCADE NOT NULL,
    carrier_percentage: DECIMAL NOT NULL, 
    FOREIGN KEY (flavor_id) REFERENCES flavors(id),
    FOREIGN KEY (bottle_id) REFERENCES bottles(id),
    FOREIGN KEY (dropper_id) REFERENCES droppers(id),
    total_cpu INT
)


CREATE TABLE cannabinoids (
    id INTEGER PRIMARY KEY,
    name VARCHAR NOT NULL,
    ppg DECIMAL NOT NULL
)

CREATE TABLE carriers (
    id INTEGER PRIMARY KEY,
    name VARCHAR NOT NULL,
    ppg DECIMAL NOT NULL
)

CREATE TABLE flavors (
    id INTEGER PRIMARY KEY,
    name VARCHAR NOT NULL,
    ppg DECIMAL NOT NULL
)

CREATE TABLE bottles (
    id INTEGER PRIMARY KEY,
    name VARCHAR NOT NULL,
    cpu DECIMAL NOT NULL
)

CREATE TABLE droppers (
    id INTEGER PRIMARY KEY,
    name VARCHAR NOT NULL,
    cpu DECIMAL NOT NULL 
)







CREATE TABLE folders (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  folder_name TEXT NOT NULL
);

CREATE TABLE notes (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  note_name TEXT NOT NULL,
  modified_date TIMESTAMP DEFAULT now() NOT NULL,
  content TEXT NOT NULL,
  folder_id INTEGER REFERENCES folders(id) ON DELETE CASCADE NOT NULL
);